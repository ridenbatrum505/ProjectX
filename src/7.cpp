
int main() {

    // Declare variables
    int x = 5;
    int y = 10;

    // Calculate sum and difference of x and y
    int sum = x + y;
    int diff = x - y;

    // Print the results
    std::cout << "Sum: " << sum << std::endl;
    std::cout << "Difference: " << diff << std::endl;

    return 0;
}